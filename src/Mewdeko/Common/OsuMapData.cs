namespace Mewdeko.Common
{
    /// <summary>
    /// Represents data extracted from an Osu! map.
    /// </summary>
    public class OsuMapData
    {
        /// <summary>
        /// Gets or sets the title of the Osu! map.
        /// </summary>
        public string Title { get; set; }

        /// <summary>
        /// Gets or sets the artist of the Osu! map.
        /// </summary>
        public string Artist { get; set; }

        /// <summary>
        /// Gets or sets the version of the Osu! map.
        /// </summary>
        public string Version { get; set; }
    }
}