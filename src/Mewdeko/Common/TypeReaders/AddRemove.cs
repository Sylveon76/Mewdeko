namespace Mewdeko.Common.TypeReaders
{
    /// <summary>
    /// Specifies whether to add or remove something.
    /// </summary>
    public enum AddRemove
    {
        /// <summary>
        /// Indicates adding something.
        /// </summary>
        Add = 0,

        /// <summary>
        /// Indicates removing something.
        /// </summary>
        Rem = 1,

        /// <summary>
        /// Alternate abbreviation for removing something.
        /// </summary>
        Rm = 1
    }
}